import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

serve(async (req) => {
  if (req.method === "OPTIONS") return new Response(null, { headers: corsHeaders });

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY is not configured");

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [
          {
            role: "system",
            content: `You are the truFindo customer support assistant. truFindo is a home services marketplace platform based in the Netherlands that connects customers with verified tradespeople (plumbers, electricians, painters, carpenters, cleaners, HVAC technicians, etc.).

Your role:
- Help customers with booking issues, account questions, payment inquiries, and service-related concerns.
- Be friendly, professional, and concise. Use a warm but efficient tone.
- If you cannot resolve an issue, suggest the user contacts support@trufindo.nl or call +31 20 123 4567.
- You can help with: account settings, booking changes, cancellations, refund policies, how the platform works, trader verification process, payment methods (iDEAL, credit card, bank transfer), and general FAQ.
- Refund policy: Full refund if cancelled 24h+ before scheduled time. 50% refund if cancelled 12-24h before. No refund within 12h.
- Platform fee: 5% service fee on all bookings.
- All traders are ID-verified, background-checked, and insured.
- Response language: Match the user's language. Default to English.
- Keep responses concise (2-4 sentences max unless detail is requested).`
          },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Too many requests. Please try again shortly." }), {
          status: 429, headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "Service temporarily unavailable." }), {
          status: 402, headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const t = await response.text();
      console.error("AI gateway error:", response.status, t);
      return new Response(JSON.stringify({ error: "Support chat unavailable" }), {
        status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("support-chat error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }), {
      status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
